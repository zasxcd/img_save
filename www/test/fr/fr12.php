<td colspan="2">分佈位置:<br>
金線側褶蛙(Pelophylax plancyi)的福建亞種，分布在台灣、中國福建、江西、韓國等地，喜歡藏身在長有水草的蓄水池或者遮蔽良好的農地，例如漂著浮萍的稻田、芋田或者茭白筍田。
</td>
