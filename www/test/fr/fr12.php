<tr>
<td colspan="2">介紹:<br>
金線蛙的體長50mm至100mm，體側綠色，兩側背側褶白色或淺綠色，中央有一條淺綠色背中線。平常棲息在靜水域，以水生動物為食。生性隱密機警，常常僅露出頭來觀察四周的動靜，若受到干擾，馬上跳入水中。在春天及夏天繁殖，雄蛙沒有鳴囊，叫聲是短促的一聲「啾」，不容易聽到。卵粒小，卵徑約1mm；每次產卵約850粒，聚成塊狀。蝌蚪褐綠色，有許多深褐色斑點。
</td>
