<td colspan="2">分佈位置:<br>
斑腿樹蛙原產於華南、香港、海南島、印度、中南半島等地區。台灣本島於2006年在彰化田尾發現，疑似卵泡附著於園藝植物從華南帶進台灣，再從彰化田尾引進到台中，擴散非常快速，可能隨著盆栽植物擴散。2012年已在台北市、新北市、桃園、台中、彰化、雲林、高雄、屏東發現其蹤跡。
