<tr>
<td colspan="2">介紹:<br>
公蛙體長29至42.3mm、母蛙體長27.至46.3mm，主要特徵為翡翠色的虹膜。膚色則會依據氣候和環境有所變化，通常是綠色伴隨些微黑褐色斑點，也有褐色的個體，即使個體上顏色有差異，依然有標誌性的綠色眼睛。碧眼樹蛙過去都被認為是1895年命名發表的艾氏樹蛙（Kurixalus eiffingeri）；後來研究團隊透過比較解剖、幼生發育形態特徵、鳴叫聲分析、分子親緣暨族群遺傳學研究等證據，以及艾氏樹蛙模式產地的樣本，確認了與艾氏樹蛙的差異。
</td>
