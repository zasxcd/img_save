<td colspan="2">介紹:<br>
以狹口蛙來說體型算是粗壯的。雌性的體長65mm，雄性較小。後肢很短，趾端方形平切狀，膨大成吸盤，會爬樹，也善於挖掘，利用足部挖洞。身體呈深棕色，有許多白色的顆粒且皮膚粗糙，斑點的外觀。在來自爪哇群島的個體中，在腹股溝區域中存在磚紅色斑點，而其他群體在該位置具有黃色斑點。
