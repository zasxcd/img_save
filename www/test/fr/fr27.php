<td colspan="2">分佈位置:<br>
分佈在嘉義、雲林、台南等地。諸羅樹蛙是一群喜歡在農耕地活動的綠色樹蛙，經常在竹林、芒草或果園發現牠們的蹤跡，特別喜歡在雨夜或大雨過後的夜晚鳴叫，所以當地人稱之為雨蛙。
</td>
