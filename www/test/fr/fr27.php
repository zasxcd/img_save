<tr>
<td colspan="2">介紹:<br>
諸羅樹蛙體長約40到80mm，背部顏色為淺黃綠色，腹部白色沒有任何斑點，身體兩側各有一條白色皮褶（白線）。三月到十月是牠們的繁殖期，但高峰期出現在七月及八月。叫聲是高而清脆的一連串「滴—、滴—、滴—」，乍聽之下，會誤以為是蟲叫。雄蛙具有單一鳴囊，鳴囊略帶黃色或橘色，相當美麗。雄蛙經常聚集一起出現，但分開停棲在遮蔽良好的植物體上鳴叫，有時候甚至會出現在二、三公尺高的地方。雄蛙鳴叫吸引雌蛙配對之後，雌蛙會帶著雄蛙到水邊落葉底下產大小如拳頭一般的白色泡沫卵塊。卵粒白色，在白色泡沫卵塊裡孵化成蝌蚪，然後藉由雨水的幫助，沖進水裡繼續完成其蝌蚪期。蝌蚪深褐色，身體扁平，尾巴尖細，約13mm，散佈著不規則的黑點。
