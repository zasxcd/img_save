<td colspan="2">介紹:<br>
史丹吉氏小雨蛙的體型小而纖細，雄性平均體長23mm、雌性約為27mm，大致呈瘦長型，頭部小，頭寬大於頭長，吻端圓鈍，口小。鼓膜及顳褶不明顯，背部顏色及花紋變化多端，一般為淺褐色或鐵灰色，有一些暗褐色或黑色粗大的砂點紋，有時會連成2-4 條縱紋。有些個體在胸側有深色斑，腹部光滑，灰白色，喉部有深褐色斑點。皮膚平滑但仍有些不明顯的小疣粒，股部內側及泄殖腔口附近的疣粒較大。前肢細長，上臂呈橘色，背面淺褐色，有深褐色斑點，指端圓無吸盤，有３個掌突。後肢不特別粗壯，有深色斑點，趾端無吸盤，趾間無蹼，僅有發達的內蹠突，無外蹠突。雄蛙具單一咽下外鳴囊。
</td>
