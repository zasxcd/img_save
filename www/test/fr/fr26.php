<td colspan="2">分佈位置:<br>
分佈於台灣、澳門、中國廣西及海南島、越南、泰國、馬來半島、婆羅州、菲律賓及印尼等地。經常棲息於海潮能到的海岸地區，並以紅樹林地區較為常見，耐鹽性較其他的蛙類高。台灣目前僅在東港及佳冬發現其蹤跡，棲息環境除了紅樹林，還包括積水的檳榔園、蓮霧園、稻田、排水溝、沼澤濕地、雞舍等潮濕的環境。白天常躲在植物根部，晚上才出來覓食，以無脊椎動物，包括螃蟹為食。
