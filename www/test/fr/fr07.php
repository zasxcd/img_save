<td colspan="2">介紹:<br>
巴氏小雨蛙是小型蛙類，雄性平均體長20mm、雌性約為20至25mm，體型略呈等腰三角形。頭部小，頭寬略大於頭長，吻端尖圓。鼓膜不明顯，有顳褶。背部主色為褐色或鐵灰色，有大塊深色鑲淺色邊的花斑。體側兩側各有一行黑色斑點，和背部大塊花斑平行。皮膚粗糙，背部及四肢都布滿疣粒，體側的疣粒較大而圓，背中央的疣粒則常成行排列。腹部白色光滑，前肢纖細有橫紋，指端有小吸盤，背面有小縱溝，有３個掌突。後肢粗壯有橫紋，趾端有小吸盤及小縱溝，趾間具微蹼，內外蹠突皆發達。雄蛙具單一咽下外鳴囊。
