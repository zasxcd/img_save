<td colspan="2">介紹:<br>中國樹蟾體型小型而細長，雄性平均體長25至35mm、雌性約為35至40mm，吻端平鈍，頭寬大於頭長，鼓膜圓。從吻端經眼睛、鼓膜到肩上方有一條深棕色眼罩。顳褶斜直明顯，背部主色為草綠色，腹部為黃色或白色，皮膚光滑。體側白色略帶黃色，散布一些大小不一的黑色斑點。前肢背面綠色，但腳掌背面非綠色而是呈黃色、白色或橘色，指端具吸盤及橫溝，指間有微蹼，掌部有小疣粒。後肢同前肢背面是綠色但掌背面也非綠色，股部內側黃色有一些小黑點，趾端也有吸盤，趾間有半蹼。內蹠突卵圓形，無外蹠突。雄蛙具單一咽下外鳴囊。
</td>
