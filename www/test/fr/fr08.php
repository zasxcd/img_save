<td colspan="2">分佈位置:<br>
黑蒙西氏小雨蛙除了分佈於台灣，也廣泛分佈於大陸南方，包括四川、雲南、貴州、安徽、江蘇、浙江、江西、湖南、福建、廣東、廣西及海南島，以及馬來半島與蘇門答臘。常見於台灣中南部及東南部一千五百公尺以下的開墾地水域及草澤。
