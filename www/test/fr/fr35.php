<tr>
<td colspan="2">介紹:<br>
牠們是綠色的中型樹蛙，體長約40至60mm。有些個體的背部還帶有一些小白點，腹面及側面散有一些黑斑點，這是牠們的老人斑，年紀越大，斑點越多。大腿內側呈鮮豔的橘紅色或淡橘色，好像穿一條紅內褲。這是一種保護色，逃跑的時候兩腿一伸，露出紅內褲來讓敵人嚇一跳。眼睛虹膜也是橘紅色的，好似感染了結膜炎。台灣北部及東北部一般在春天及夏天繁殖；中南部在夏天及秋天產卵；東部花蓮地區是在冬季；潮溼的山區例如溪頭，則終年繁殖。平常住在樹林裡，繁殖期時才到水邊活動。繁殖期的時候，雄蛙常在落葉底下挖一個淺淺的洞藏身鳴叫，也喜歡躲在水溝旁邊的石縫、鬆鬆的土堆或草根處，有時也會爬到樹上高歌。雄蛙有單一外鳴囊，叫聲響亮，如同火雞叫般的一長串「呱—阿呱呱呱呱呱呱」。雌蛙受雄蛙叫聲吸引，主動接近雄蛙形成配對，偶而也會出現一隻雌蛙同時和多隻雄蛙交配的情形。卵塊是白色泡沫狀，直徑約八公分，常產於植物根部或爛泥堆中。蝌蚪大型黑色，身體橢圓形，尾長為體長兩倍。
</td>
