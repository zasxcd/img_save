<td colspan="2">分佈位置:<br>
莫氏樹蛙廣泛分布於全省從低海拔的樹林、果園、開墾地到兩千多公尺高山針葉林，都可見到牠們的蹤跡，是台灣分布最廣的樹蛙，也因為是特有種，所以曾被列為保育類，但在2008年因族群量穩定，降為一般類。莫氏樹蛙的繁殖期隨地區而有所不同，但以春天比較多。
</td>
