<td colspan="2">介紹:<br>
腹部呈橘紅色而得名，背部為綠色，體長約50至80mm。棲息在高大喬木上，生性隱密，不容易發現。繁殖期以春夏兩季為主，雄蛙有單一外鳴囊，叫聲是小聲不連續的「呱」。產泡沫型卵塊於大型樹洞中，或樹林底層的靜水域。蝌蚪大型黑色，底棲型，口位於腹側，眼睛位於頭部上方，尾鰭高。
