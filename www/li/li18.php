<td colspan="2">分佈位置:<br>廣泛分布於臺灣本島北部及宜蘭1000公尺以下之低海拔區域。主要棲息於草生地、灌叢區，或是會在較高大的禾本科植物區活動。翠斑草蜥是以白天活動為主的蜥蜴，而夜間則會攀附在植物葉面上休息。</td>
</tr>

<tr>
<td colspan="2">介紹:<br>體長約50mm，最大全長可達240mm的蜥蜴，翠斑草蜥是台灣草蜥的特徵相近，雌蜥全身褐色，但是雄蜥在生殖季節會在體側出現金屬光澤的翠綠色斑點，因而得名。有二對鼠蹊孔，下頷三對頦片，在2008年後才與翠斑草蜥從台灣草蜥中被個別分類出來。鹿野草蜥是以卵生方式生殖的蜥蜴，一次可產2到6枚。以昆蟲或其他無脊椎動物為食。
