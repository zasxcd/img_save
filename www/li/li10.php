<td colspan="2">分佈位置:<br>南亞、東南亞、台灣、菲律賓群島、印澳群島及大洋洲一帶，美國夏威夷亦因人為引入而建立了族群。僅在台灣的高雄、屏東、台東以及蘭嶼、綠島等地有發現記錄，數量少，主要棲息於低海拔近海岸樹林，屬夜行性，白天喜藏於林投、檳榔、香蕉等寬葉植物的葉片基部、樹皮下或樹縫中。</td>
