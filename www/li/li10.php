<tr>
<td colspan="2">介紹:<br>軀幹長約40mm全長約80mm，尾長與吻肛長約相等；身體細長且扁平，尾部圓筒形；四肢短，第一趾萎縮變小，但有微小的爪，其餘各趾趾端膨大，趾下皮瓣2列，且均有爪；有1深褐色過眼線，由鼻孔經眼睛、肩部到胸部側面，身體背面灰褐色，並混有深色雜斑，尾部顏色常與身體不同為黃褐色。到目前為止對本種的研究發現，本種在亞洲東部的島嶼區只有雌性個體，沒有雄性個體，行孤雌生殖，雌性個體每次可產2個白色的卵，約68天後孵化。尾部易自割，以昆蟲及其他小型節肢動物為食。
