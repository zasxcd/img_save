<tr>
<td colspan="2">分佈位置:<br>僅分布於馬祖的北竿與南竿；此外，於中國南部向北延伸到吉林，向西到甘肅都有其分布。主要的棲息環境為草生地、灌叢區，白天活動為主的蜥蜴，而夜間則會在植物葉面上休息。</td>
</tr>

<tr>
<td colspan="2">介紹:<br>最大體長可達70mm，最大全長近300mm的蜥蜴，體背顏色主要以褐色為主，腹面為白色。其眼後方常有一白色或淺色線經體側延伸至尾基部，形成兩條明顯的縱線，而體側則主要為綠色。體型細長的北草蜥，一般而言具有一對鼠蹊孔，舌頭有著明顯的分叉。北草蜥為以卵生方式生殖的蜥蜴，一窩可產下卵2至6枚。是以昆蟲或其他無脊椎動物為食。
