<tr>
<td colspan="2">介紹:<br>軀幹長約50至60mm，最大全長可達180mm，體型較小的石龍子。體背主要為褐色或橄欖綠色的臺灣蜓蜥，背後有許多的暗色雜斑分布，體側由吻端經過眼而延伸至尾之基部有一深褐色寬縱帶。體側面接近腹面處顏色較淺，其上也有不規則的深色斑塊的分布，腹面則為黃色或黃綠色。臺灣蜓蜥是以卵生方式生殖的蜥蜴，每窩可產下卵 2到7枚。以昆蟲與小型無脊椎動物為主食，也是極容易自割尾部的蜥蜴。
