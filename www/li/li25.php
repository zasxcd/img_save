<td colspan="2">分佈位置:<br>廣泛分布在臺灣本島各地1800公尺以上之中海拔至高海拔區域。主要棲息於裸露地、石縫、灌叢區，屬於日行性的蜥蜴。白天常出沒於開闊地的臺灣蜓蜥，受到干擾時會迅速找尋遮蔽物躲藏。</td>
