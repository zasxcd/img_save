<td colspan="2">分佈位置:<br>分布於臺灣本島東部1100至1800公尺的中海拔山區，目前只侷限在宜蘭南澳、三星以及花蓮等地有發現紀錄。為樹棲性蜥蜴，喜好的棲息環境為樹林邊緣，常可於日間觀察到牠們出現在森林邊緣空曠處活動。呂氏攀蜥具有明顯的領域行為，當有外物接近時，常可觀察到雄性個體有喉部擴張，持續做出伏地挺身的威嚇動作。</td>
