<tr>
<td colspan="2">介紹:<br>體長可達100mm，最大全長約270mm的蜥蜴，體色鮮艷、尾部不會自割的呂氏攀蜥，嘴的外緣為黃色。呂氏攀蜥屬於雌雄二型性顯著的物種，雌雄外表明顯不同，體色主要以綠色為主，但顏色會隨著環境不同而作小幅度的改變。雄性個體背部有大面積的黑色或暗褐色區塊，且具有較明顯的鬛鱗，背部除了有黃綠色斑點或線條的分布外，兩側尚有由菱斑連貫而形成的黃綠色縱帶；雌性個體雖然體色主要以綠色為主，但有些個體從頭部經背脊中央延伸到尾巴均為暗褐色，形成一條寬縱帶。呂氏攀蜥是以卵生方式生殖的蜥蜴，生殖季節以夏季為主，一窩可產下卵4至6枚。日行性的呂氏攀蜥，主要以昆蟲或是其他小型無脊椎動物為食。</tr>
