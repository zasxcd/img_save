<tr>
<td colspan="2">介紹:<br>軀幹長約40mm，最大全長可達80mm，身體主要以褐色為主，體色深淺會隨環境而改變。由體背延伸到尾部的淺色區域會形成背部中央規則分布的斑塊。尾部邊緣鱗片特化為細刺狀，且雄性個體尾巴基部明顯突起。四肢指下有指瓣兩列，第一指不具爪且第三、第四指間具蹼狀膜。雅美鳞趾蝎虎是一種以卵生方式進行生殖的壁虎，一次會產下兩枚卵殼緊密相連的卵。尾部極容易自割，以昆蟲和其他小型無脊椎動物為食。
