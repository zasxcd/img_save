<tr>
<td colspan="2">介紹:<br>長約80mm，最大全長可達220mm的石龍子科蜥蜴。體背主要為褐色的股鱗蜓蜥，背後有許多的暗色雜斑分布，體側由吻端經過眼而延伸至尾之基部有一黑色寬縱帶。幼體尾巴末端呈紅色的股鱗蜓蜥，雖然在外型上與印度蜓蜥十分相似，但除了在大腿股部有著鱗片不規則排列的區域外，體腹為白色的股鱗蜓蜥，也可以腹部顏色來與體腹黃色的印度蜓蜥作區別。股鱗蜓蜥為以卵生方式生殖的蜥蜴，每窩可產下卵1到6枚。尾巴極容易自割，為一種以昆蟲與其他小型無脊椎動物為食的日行性蜥蜴。
