<td colspan="2">分佈位置:<br>廣泛分布於臺灣本島2500公尺以下的山區，以及龜山島、澎湖、金門、馬祖等離島區域，是臺灣產石龍子中分布最廣的種類。主要的棲息環境為居家庭園、墾地或大面積草生地，當遇到干擾便快速逃逸或是藏身在石塊、土洞、排水管中。</td>
