</tr>

<tr>
<td colspan="2">分佈位置:<br>只被發現在500公尺以下之低海拔地區，於陽明山、花蓮市與台東紅葉都有零星的觀察紀錄。此外，中國的雲南、海南島、香港；非洲馬達加斯加經印度、東南亞、菲律賓到琉球，以及大洋洲也都有其分布。牠多半出沒於房舍附近遠離光照的區域，是一種夜間活動的蜥蜴。</td>
</tr>

<tr>
<td colspan="2">介紹:<br>軀幹長約60mm，最大全長可達120mm，其體色會隨著環境而改變，體背除了具有些許圓形斑點外，主要為較淺的灰色或接近白色。其尾部基部肥厚，且週緣具有鋸齒狀的鱗片，四肢指下有雙列指瓣。裂足蝎虎是以卵生方式生殖的壁虎，一次產下兩枚緊密相連的卵。尾部極容易自割，以昆蟲及其他小型無脊椎動物為食。
</tr>
