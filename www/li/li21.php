<td colspan="2">分佈位置:<br>分布於中國大陸長江以南地區、海南島及越南的南丁格爾島，台灣僅見於金門大膽島。生活於低海拔山區、平原耕作區。分布範圍從平地到海拔1000公尺左右都有發現。</td>
</tr>

<tr>
<td colspan="2">介紹:<br>體長約150mm，最大全長可達360mm的蜥蜴。鱗片無鱗脊的中國石龍子，外表因此而顯得光滑。其幼體與成體體色不同，幼體體背黑色，背部及體側有許多淺褐色斑點，體腹部為淺褐色或乳白色，尾部末端則為藍色；體色會隨著成長而改變，體型也逐漸變得粗壯，身體呈土黃色或淡黃色，體背呈淡灰褐色，體側邊近前肢處有許多紅色斑點，中國石龍子是以卵生方式生殖的蜥蜴，曾經有一次產下19枚卵的生殖紀錄。以小型無脊椎動物為食。
</tr>
