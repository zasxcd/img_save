</tr>

<tr>
<td colspan="2">介紹:<br>軀幹長約100mm，最大全長可達200mm的壁虎，其體背主要以褐色為主，有著深淺不一的斑點。其中從頸部到尾部有十數對兩兩成對的深色斑，形成背部較特殊的特徵，背部的鱗片兼具有細小的粒鱗與大型的疣鱗。尾部有許多櫛刺狀的鱗片，以環狀的方式排列，四肢指端明顯擴張，指下有單列的指瓣。菊池氏壁虎是以卵生方式生殖的壁虎，一次會產下兩枚緊密相連的卵。尾部極容易自割的菊池氏壁虎，以昆蟲及其他小型無脊椎動物為食。
</tr>
