<td colspan="2">分佈位置:<br>僅分布在花東地區棲息於海拔500公尺以下，要棲息於草生地、芒草區或灌木叢中，白天常出現在草叢中或芒草區邊緣的開闊地帶活動，夜晚則會在芒草葉面或其他植物體上休息。</td>
