<tr>
<td colspan="2">介紹:<br>體長約60mm，最大全長可達240mm的蜥蜴，鹿野草蜥是台灣草蜥的特徵相近，身體褐色為主，體側具淺色縱線，有二對鼠蹊孔，下頷三對頦片，在2008年後才與翠斑草蜥從台灣草蜥中被個別分類出來。鹿野草蜥是以卵生方式生殖的蜥蜴，一次可產2到3枚。鹿野草蜥是日行性的蜥蜴，主要以捕捉昆蟲或是其他小型無脊椎動物為食。臺灣草蜥生性機警，當遭遇天敵捕捉或觸碰時，常會有自割尾部的行為，隨後即迅速鑽入草叢中以逃避敵害。
