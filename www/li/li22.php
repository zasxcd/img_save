<td colspan="2">分佈位置:<br>分布於臺灣全島1000公尺以下的低海拔山區，屬於發現頻率不高的種類，但在某些區域內數量尚稱豐富，為區域性普遍的蜥蜴。主要的棲息環境為居家庭院、墾地或大面積的開闊草生地，偶爾可於樹林底層發現其蹤。當遇到干擾時便會快速逃逸或是藏身在石塊、土洞中。</td>
