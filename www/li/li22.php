<tr>
<td colspan="2">介紹:<br>體長約130mm，最大全長可達330mm的蜥蜴。鱗片無鱗脊的臺灣中國石龍子，外表因此而顯得光滑。其幼體與成體體色不同，幼體體背黑色，背部及體側有許多淺褐色斑點，體腹部為淺褐色或乳白色，尾部末端則為藍色；體色會隨著成長而改變，體型也逐漸變得粗壯，成體體背轉為深褐色或灰褐色，鱗片邊緣為黑色，體側面則常有黑色或橘紅色的雜斑。臺灣中國石龍子是以卵生方式生殖的蜥蜴，曾經有一次產下19枚卵的生殖紀錄。以昆蟲與其他小型無脊椎動物為食的臺灣中國石龍子，屬於日行性的蜥蜴，其尾巴極容易自割。
