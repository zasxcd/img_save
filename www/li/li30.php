<td colspan="2">介紹:<br>最大體長約90mm，最大全長可達220mm，身體背面主要為褐色，背上鱗片具有明顯鱗脊，且背部近尾基處有許多黑色斑點。其身體左右側面各有一條黑色寬縱帶由吻端經眼部延伸至尾基部，縱帶上下邊緣均為淺褐色，身體腹面則為乳白色。多稜南蜥是以卵生方式生殖的蜥蜴，每窩可產下2到3枚卵。為日行性的蜥蜴，其尾巴極容易自割。
