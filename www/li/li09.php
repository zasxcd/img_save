<td colspan="2">分佈位置:<br>史丹吉氏蝎虎主要分布於臺灣本島1000公尺以下之低海拔地區，目前僅在南投集集、竹山、合社、嘉義、花蓮紅葉、高雄扇平有發現紀錄。此外，菲律賓及越南南部亦有分布。常出沒於郊區之居家屋舍附近，是一種主要在夜間活動的蜥蜴。</td>
</tr>

<tr>
<td colspan="2">介紹:<br>軀幹長約60mm，最大全長可達120mm，身體主要由或深或淺的褐色、灰色所構成。體背有著許多淺色斑塊，體背中央的圓斑延伸到尾部形成環狀的斑節，而尾部特化的鱗片使得尾巴兩側呈現鋸齒狀。四肢指下有雙列的指瓣，且第一指有爪。史丹吉氏蝎虎是以卵生方式生殖的壁虎，一次會產下兩枚卵殼間不相連的卵。尾部極容易自割，以昆蟲和其他小型無脊椎動物為食。
