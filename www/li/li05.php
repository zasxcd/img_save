<td colspan="2">分佈位置:<br>廣泛分布於臺灣全島1000公尺以下之低海拔地區，以及蘭嶼、龜山島兩處離島，是臺灣地區最常見的守宮。此外，在日本九州、琉球：中國江蘇、福建到湖南、貴州亦有其發現紀錄。主要出沒於郊區房舍或樹林中，是一種主要在夜間活動的蜥蜴。</td>
</tr>

<tr>
<td colspan="2">介紹:<br>軀幹長約60mm，最大全長近130mm，體背主要為褐色或深褐色，而暗褐色與淺褐色規則分布的斑塊，由頸部背部延伸到尾部，形成體背中央極明顯的特徵。在體背較細小的粒鱗間，夾雜有較大型的疣鱗，擴展的前後肢指端下有單列指瓣。鉛山壁虎是以卵生方式生殖的壁虎，一次產下兩枚緊密相連的卵，且有聚集產卵的現象。
