<td colspan="2">介紹:<br>體長近80mm，最大全長可達170mm的石龍子科蜥蜴。四肢相對較短小、頸部不明顯的中國光蜥，體背部主要為暗褐色，體側面顏色較淺且從頸部到尾部都有白斑的分布。中國光蜥是以卵生方式生殖的蜥蜴，一窩可產下卵5枚。日行性的中國光蜥，主要以昆蟲、小型無脊椎動物為食。
