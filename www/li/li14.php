<tr>
<td colspan="2">介紹:<br>體長約60mm，最大全長可達220mm的蜥蜴，一般具有二對鼠蹊孔。舌頭有著明顯的分叉，體背面以褐色為主的臺灣草蜥，幼體體側在眼下方為淺色或白色，此淺色區向後方延伸到尾部形成體側的縱帶，待成長時此淺色縱帶會逐漸轉為黃綠色。臺灣草蜥是以卵生方式生殖的蜥蜴，每次可以產下卵2到6枚。臺灣草蜥是日行性的蜥蜴，主要以捕捉昆蟲或是其他小型無脊椎動物為食。臺灣草蜥生性機警，當遭遇天敵捕捉或觸碰時，常會有自割尾部的行為，隨後即迅速鑽入草叢中以逃避敵害。
