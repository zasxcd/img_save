<td colspan="2">分佈位置:<br>廣泛分布於臺灣本島西半部以及宜蘭縣1000公尺以下之低海拔區域；此外，在外島澎湖也有觀察紀錄。主要棲息於草生地、灌叢區，或是會在較高大的禾本科植物區活動。蓬萊草蜥是以白天活動為主的蜥蜴，而夜間則會攀附在植物葉面上休息。</td>
</tr>

<tr>
<td colspan="2">介紹:<br>最大體長可達60mm，最大全長約250mm的蜥蜴，體背顏色主要以褐色為主，腹面為白色。眼後方常有一白色或淺色線經體側延伸至尾基部，形成兩條明顯的縱線，且縱線下方常為呈現綠色的區塊。體型細長的蓬萊草蜥，一般而言具有一對鼠蹊孔，舌頭有著明顯的分叉。蓬萊草蜥為以卵生方式生殖的蜥蜴，一窩可產下卵2至6枚。以昆蟲或其他無脊椎動物為食。
