<td colspan="2">介紹:<br>體長約110mm，最大全長可達280mm的蜥蜴。其外表由於全身鱗片無鱗脊而顯得格外光滑。其幼體與成體體色不同，幼體體背為黑色，背部及體側有許多黃褐色斑點，體腹部為淺褐色或乳白色，尾部末端為藍色；成體體型粗壯，體色也會隨著成長而逐漸轉變，體背漸變為灰色或灰褐色，鱗片邊緣為黑色，全身則有許多淺色或白色的雜斑。白斑石龍子應該是以卵生方式生殖的蜥蜴，唯生殖資料尚欠缺。以昆蟲與其他小型無脊椎動物為食的中國石龍子，屬於日行性的蜥蜴，其尾巴極容易自割。
