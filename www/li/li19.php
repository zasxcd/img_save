<tr>
<td colspan="2">分佈位置:<br>廣泛分布於臺灣全島1000公尺以下之低拔山區，屬於局部區域普遍的蜥蜴。此外，在中國的廣東、海南、廣西、貴州也有其分布。主要棲息於樹林、灌叢區，是一種攀爬能力極強的樹棲性蜥蜴，常可於樹幹上、灌叢間觀察到其活動。古氏草蜥是日行性的蜥蜴。</td>
</tr>

<tr>
<td colspan="2">介紹:<br>體長可達60mm，最大全長可達250mm的蜥蜴。舌頭有著明顯的分叉，具有三對或三對以上鼠蹊孔的古氏草蜥，體背面以褐色為主，背部中央的暗色鱗片常形成兩列縱線，腹面則為白色。體側面顏色稍淺，且有深色縱帶由吻端經眼睛、體側延伸到尾基部。細長的吻端，則是另一項可作為區分古氏草蜥與其他草蜥的特徵。古氏草蜥是以卵生方式生殖的蜥蜴，曾經有一窩產下一枚卵的觀察紀錄。
