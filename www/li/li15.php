<td colspan="2">分佈位置:<br>目前的發現區域主要是在臺灣本島中部靠近雪山山脈的1800至3500公尺之中高海拔山區。主要棲息於高海拔碎石坡、灌叢或箭竹林邊緣，白天常會出現在草叢邊緣的開闊地活動或曬太陽，稍微受到驚擾即迅速鑽入岩石縫穴或灌叢中。</td>
</tr>

<tr>
<td colspan="2">介紹:<br>體長約70mm，最大全長可達190mm的蜥蜴，一般具有二對鼠蹊孔。舌頭有著明顯的分叉，體背面以褐色為主的雪山草蜥，腹面則為白色，體側左右各有一深色寬縱帶，且縱帶邊緣的淺色區形成兩條延伸到尾基部的縱線。腹鱗平滑、尾部較短，也是雪山草蜥與其他草蜥明顯不同之處。雪山草蜥是以卵生方式生殖的蜥蜴，每次可以產下卵2到6枚。有時也會在有遭遇天敵捕捉或觸碰時而有尾部自割的行為。雪山草蜥是日行性的蜥蜴，主要以捕捉昆蟲或是其他小型無脊椎動物為食。
