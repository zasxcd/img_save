<tr>
<td colspan="2">介紹:<br>
是狹口蛙家族裡的巨無霸，體長平均7公分。皮膚厚，光滑，但有一些圓形顆粒。背部棕色，有一個深棕色大三角形斑，看起來很像一個花瓶。趾端方形平切狀，膨大成吸盤，會爬樹，也善於挖掘，利用足部挖洞，僅需數秒鐘即可將身體埋入土中。雄蛙有單一外鳴囊，叫聲短促低沈。受刺激時，會鼓氣，甚至分泌白色毒液，因此天敵不多。蝌蚪期很短，不到三星期，成蛙壽命約6年。從以上觀察得知，牠在野外已經有生殖族群，雖然是新移民，但競爭力很強，和台灣其他蛙類的關係，值得長期觀察。
</td>
