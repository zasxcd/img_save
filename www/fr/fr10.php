原產地為中國南部，以及泰國、印度、馬來西亞、新加坡等國。台灣最初約於1997～1998年發現，首次現身地方為高雄縣林園鄉與鳳山水庫一帶，目前則主要分佈在台灣南部的台南與高屏地區，其他縣市並未發現其蹤跡。
