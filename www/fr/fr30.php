<td colspan="2">分佈位置:<br>
主要分佈於台灣中部以北的中低海拔山區及平地。居住在樹上或樹林底層，繁殖時期雄蛙才會遷移到樹林附近的靜水域，並在水邊的草根、石縫或落葉底下挖洞鳴叫。洞直徑約5公分，由於上覆有遮蔽物，所以不容易看到。
