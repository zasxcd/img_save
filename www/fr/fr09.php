<tr>
<td colspan="2">介紹:<br>
小雨蛙體型非常小，雄性平均體長25mm、雌性約為20至30mm，身體呈等腰三角形，頭長約等於頭寬，口小且吻端尖圓。鼓膜不明顯，顳褶明顯。背部顏色為土褐色或灰棕色，背中央有一塊明顯的深色對稱塔狀花紋，花紋兩側另有一些平行的細縱紋，有些個體有淺色背中線。身體兩側從吻端經眼後方到腰部有黑色斜行的縱紋，縱紋下方有黑色小斑點。皮膚略光滑但有些小疣粒凸起，尤其從眼後到薦骨突起處，兩側各有一排斜行的長疣粒。腹部是光滑白色，前肢纖細，有粗細不等的橫紋，指端圓無吸盤，指間無蹼，內掌突比外掌突大。後肢粗壯有橫紋，趾端無吸盤，趾間無蹼，內蹠突略大於外蹠突。雄蛙具黑色單一咽下外鳴囊。
</td>
</tr>
