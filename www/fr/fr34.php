<td colspan="2">介紹:<br>
公蛙體長28.6至31.6mm、母蛙體長30.8至37.1mm，王氏樹蛙過去都被認為是1895年命名發表的艾氏樹蛙（Kurixalus eiffingeri）；後來研究團隊透過比較解剖、幼生發育形態特徵、鳴叫聲分析、分子親緣暨族群遺傳學研究等證據，以及艾氏樹蛙模式產地的樣本，確認了與艾氏樹蛙的差異。
</td>
