<tr>
<td colspan="2">分佈位置:<br>廣泛分布於台灣全島。分布山區的海拔高度可達3000公尺，在平地，常出現於闊葉林、草地、開墾地或住家附近，常常可見到在步道、空地或路燈底下比較亮、蟲比較多的地方覓食；繁殖期時才會遷移到溪流或淨水池。</td>
</tr>

<tr>
<td colspan="2">介紹:<br>體型屬大型，身長在50到200mm之間，大多數約60到110mm，體色多樣，個體間大小差異極大，母蛙也明顯比公蛙大上許多，頭部圓鈍，具耳後腺，耳後腺下方有一黑線，皮膚粗糙而有肉疣狀凸起，體色變異大，以黃褐色、紅褐色或黑褐色為主，身上花紋變化多，個體間差異極大，還會隨著環境而便深變淺。前趾無蹼但後趾間有蹼，部分個體有背中線。公蛙不具鳴囊。
</tr>
