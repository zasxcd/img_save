<td colspan="2">分佈位置:<br>
分佈於台灣，中國南部、印度及印尼等地。常見於台灣北部及西部一千公尺以下的山區路旁水溝或小溪，喜歡住在遮蔽良好、溝底有落葉淤泥的淺水溝或溪澗中，白天也看得到，但多半躲在落葉底下。
</td>
