<td colspan="2">介紹:<br>
體長約40至60mm。澤蛙的上下唇有深色的縱紋，如同畫上紋面的原住民。背部有許多長長短短的棒狀突起，顏色及花紋多變，讓人覺得每一隻長得都不一樣。一般是褐色或深灰色，有時雜有明顯的紅褐色或綠色斑紋。有些個體在背部中間有一條金色背中線，非常的醒目，難怪學名稱為「沼澤裡的美麗蛙類」。雄蛙有單一鳴囊，但因鳴囊中間有一分隔，所以看起來很像一對外鳴囊。春夏兩季比較容易聽到牠們時高時低、變化多端叫聲。單獨一隻鳴叫時，叫聲是連續數十個「嘓、嘓...」，意思是我愛你；但在兩隻對叫的時候，叫聲則變成「嘓嘓-嘓嘓-」或「嘓嘰、嘓嘰」，那是競爭比試意思。卵成片漂浮在水面上，蝌蚪上下唇有3條明顯的白線。
</td>
