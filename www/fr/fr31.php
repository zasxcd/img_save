<td colspan="2">分佈位置:<br>
僅分布於台灣全島中低海拔的山區，平時喜歡棲息在河邊的樹上或石縫中，此時的顏色較淺，幾乎呈現白色。春夏季節，則會趁著黑夜，成百上千隻遷移到溪流裡繁殖，聚成小群在大石頭上鳴叫，雄蛙此時經常變成金黃色。
