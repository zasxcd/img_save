<tr>
<td colspan="2">介紹:<br>
"褐樹蛙體長50到90mm，在樹蛙家族中屬於中大型。雄蛙及雌蛙的體型差異很大，這是溪流繁殖蛙類的特性之一。身體呈褐色，從兩眼到吻端有一塊淡黃色的三角形斑，而兩眼到體背另有一塊倒三角形的黑斑。眼睛大而突出，虹膜有銀白色及褐色兩色，好像英文T字，雄蛙有單一外鳴囊，經常聚成小群在石頭上鳴叫，叫聲是細碎的「嘓、嘓」，偶而會發出幾聲粗粗的「嘎」。褐樹蛙因為是特有種而且有捕捉壓力，所以曾被列為保育類，但在2008年因族群量穩定，降為一般類。繁殖期5月到8月，以夏天為主，此時白天仍在溪邊的樹上或石縫休息，黃昏的時候，才一隻一隻的跳到岸邊水流較緩的地方進行繁殖活動。雌蛙體型很大，產卵前，雌蛙會先伸腿交互踢幾下，然後產一小團卵粒，雄蛙也會配合雌蛙動作伸長身體，以提高授精率，每次產卵300-400粒，卵小型、一粒粒分離黏在石頭底下，通常聚成一堆。蝌蚪為底棲性，通常吸附在石頭上，出現於溪邊水流較緩的地方。"
</td>
</tr>
