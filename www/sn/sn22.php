<td colspan="2">分佈位置:<br>
在臺灣，福建頸斑蛇是直到1996年才在陽明山區被發現的新記錄蛇種，目前只在陽明山地區有零星的發現紀錄，數量甚少。在中國，福建頸斑蛇則廣泛分布於西南、西北和中南部地區。主要棲息於山區的林木底層，生性隱蔽，數量很少。以蚯蚓或無脊椎動物為食。
</td>
</tr>

<tr>
<td colspan="2">介紹:<br>
福建頸斑蛇是一種中小型的蛇類，最大全長可達90公分，頭部細小，頸部不明顯且其上有一黑色橫帶。全身呈以橄欖灰色為主，有一些細小的黑色雜斑，尾部極短。福建頸斑蛇是以卵生方式生殖的蛇類，於夏季產卵，每窩可產下卵5到11枚。
