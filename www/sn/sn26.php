<td colspan="2">分佈位置:<br>
黑頭蛇廣泛分布在低海拔至高海拔山區，臺灣本島2000公尺以下區域都可發現。此外，也分布於中國南部地區和越南北部。主要棲息於山區林地或開墾地，是一種動作緩慢，性情溫馴的蛇類。黑頭蛇於白天活動，主要以蜥蜴和其他蛇類為食。
</td>
</tr>

<tr>
<td colspan="2">介紹:<br>
黑頭蛇是一種小型的蛇類，最大全長可達72公分，細長的身體呈黃褐色，其頭頂灰黑色，頭頸部有明顯的黑色紋，在黑色橫紋後方有一黃色的細橫紋，且上唇至頭頸部有一黃白色的細縱紋。黑頭蛇是以卵生方式生殖的蛇類，於春末至夏季間產卵，每窩可產下卵2到6枚。
