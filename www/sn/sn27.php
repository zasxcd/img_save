<td colspan="2">分佈位置:<br>
白腹游蛇廣泛分布於臺灣全島1500公尺以下之中低海拔地區。此外，在中國南部地區、緬甸和越南北部也有分布。主要棲息於溪流湖泊，特別是在乾淨的水域較易發現。日夜都會活動的白腹游蛇，屬於動作迅速、攻擊性強的蛇類。以蝦子、蝌蚪、蛙類和魚類這些水棲生物為食。
</td>
</tr>

<tr>
<td colspan="2">介紹:<br>
白腹游蛇是一種中型的蛇類，最大全長可達1公尺，在外型上與赤腹游蛇相似，但是頭部比赤腹游蛇尖細。全身棕黑色的白腹游蛇，身體有著不規則的黑色橫紋，但這橫紋於幼蛇或剛蛻皮的成蛇身上較為明顯；腹部為灰白色，並有著黑褐色的大型斑紋交錯排列或相連成一條橫帶。白腹游蛇是以卵生方式生殖的蛇類，於夏季產卵，每窩可產卵4到25枚，出生仔蛇約17公分。
