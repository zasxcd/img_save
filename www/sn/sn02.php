<td colspan="2">分佈位置:<br>
台灣黑眉錦蛇廣泛分布於臺灣本島2000公尺以下之中低海拔地區，主要棲息環境包括了山區、平地、樹林及草地。剛接觸時有些個體會展現強烈的攻擊行為。錦蛇屬於日行性的蛇類，以蛙、鳥類、鳥蛋和鼠類等小型哺乳類為食。
</td>
</tr>

<tr>
<td colspan="2">介紹:<br>
錦蛇是一種大型蛇類，最大全長可達近3公尺，身體主要呈橄欖黃色，前段常有規則的黑色菱形斑，中段黑斑略呈十字型，後段背部有二條黃色縱紋，眼睛後方起至頭後方有一明顯的黑色縱帶，故有黑眉錦蛇之稱。錦蛇是以卵生方式生殖的蛇類，於每年5月左右交配，夏季產卵，每次可產卵2到13枚，約1個月孵化，溫度低時，孵化期可延長至2個半月，仔蛇全長約39公分。
