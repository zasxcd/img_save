<td colspan="2">分佈位置:<br>
梭德氏游蛇主要分布在臺灣全島1500公尺以下之中低海拔山區，此外，於中國南部地區、北越也有其分布，主要棲息於潮濕環境的梭德氏游蛇，以蛞蝓、蚯蚓、蝌蚪與蛙類為食。不管白晝或是夜裡，都有可能觀察到牠們活動。
</td>
</tr>

<tr>
<td colspan="2">介紹:<br>
梭德氏游蛇是一種小型蛇類，最大全長可達76公分。其體背與腹面顏色成強烈的對比，背面為暗褐色、腹面黃綠色，背、腹交界處有黑斑從頸部延伸至尾端。上唇鱗片為白色，並有淡色條紋延伸至頸部形成V。梭德氏游蛇是以卵生方式生殖的蛇類，有於春末產卵5枚的觀察紀錄。
