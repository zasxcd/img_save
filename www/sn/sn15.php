<td colspan="2">介紹:<br>
紅斑蛇是一種中型蛇類，最大全長約160公分，紅棕色的身體上有大型的黑色橫斑。有些個體體側的紅棕色加深為黑色而和黑色橫斑連結，這時身體便呈現黑棕色，而有規則的紅棕色細橫帶。紅斑蛇為卵生的蛇類，於春夏季兩季生殖，每窩約產卵8枚，有些個體一年可產2窩，於春夏季各產一窩，孵化期約1個月，仔蛇全長約23公分。
