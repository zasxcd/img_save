<td colspan="2">分佈位置:<br>
紅斑蛇廣泛分布於臺灣全島2000公尺以下中低海拔地區及離島馬祖。此外，全中國(除內蒙古、新疆和西藏外)和寮國及北越都有發現紀錄，韓國、日本也有分布。廣泛棲息於各類環境，包括樹林底層、農田、水域及住宅區各類環境，都不難發現其蹤跡，因而可算是在台灣最容易發現的夜行性蛇種。棲地型態廣泛的紅斑蛇，食性亦廣，魚、蛙、蟾蜍、蜥蜴、蛇、鳥、鼠都是牠的食物，也曾有攝食同種蛇蛋之紀錄，但主要以兩生類與小型鼠類為食。初遭遇時攻擊性強，且被捕捉時常會由肛門腺或泄殖腔排出具惡臭的分泌物。
</td>
