<td colspan="2">分佈位置:<br>
鐵線蛇主要分布於臺灣全島1500公尺以下之中低海拔地區，蘭嶼亦有其分布。此外，從中國西南、印度東北邊、東南亞到日本的琉球群島也都有鐵線蛇的分布，主要棲息於林木底層的落葉或枯木堆裡，為穴居性的蛇類，隱密性高，不易發現。以小型無脊椎動物如昆蟲和蚯蚓為食。關於鐵線蛇的生活史，目前尚不清楚。
</td>
</tr>

<tr>
<td colspan="2">介紹:<br>
鐵線蛇是一種小型的蛇類，最大全長不超過40公分，眼睛小、頭小而頸部不明顯。其身體主要以紅棕色或棕黑色為主，有8到10條隱約可見的細縱紋，在陽光下會呈現有金屬光澤。尾部很短，尾端驟縮成角質化尖刺狀。鐵線蛇的生殖狀況不明。
