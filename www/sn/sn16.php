<td colspan="2">分佈位置:<br>
白梅花蛇廣泛分布於臺灣全島1500 以下之中低海拔地區。此外，中國的西南和中南地區及日本的南琉球群島都有分布。棲息環境主要以山區或開墾地附近為主。以夜間活動為主的白梅花蛇，擅長於爬樹，以蜥蜴及昆蟲類食物為主食。
</td>
</tr>

<tr>
<td colspan="2">介紹:<br>
白梅花蛇是身體極為細長的中型蛇類，最大全長可達110公分，黑白相間的環紋雖然與雨傘節的環紋相似，但其白色環紋愈往身體後方愈寬，常和黑色環紋一樣寬或比黑色環紋寬。此外，白梅花蛇的白色橫紋中亦雜有灰色橫紋，且環紋破碎不規則，不若雨傘節的環紋般規則，可以從這些特徵來與雨傘節區辨。白梅花蛇是以卵生方式生殖的蛇類，唯生殖季節不明。
