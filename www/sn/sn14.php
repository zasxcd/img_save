<td colspan="2">分佈位置:<br>
在臺灣，灰腹綠錦蛇的數量很稀少，直到最近才在屏東大漢山發現，目前已知的灰腹綠錦蛇分布範圍主要以臺灣本島的南部與東部。此外，在中國南方、印度和北越也都有此種蛇類的分布。主要棲息於樹林，以白天活動為主，捕食蜥蜴、鳥類和鼠類。初遭遇時灰腹綠錦蛇會展現強烈的攻擊行為。
</td>
</tr>

<tr>
<td colspan="2">介紹:<br>
灰腹綠錦蛇是一種中型的蛇類，最大全長可達150公分。全身的翠綠色雖然和青蛇有些相似，但吻端較尖且眼後至後頸部有一條黑色縱帶，故不難與青蛇區辨。灰腹綠錦蛇的幼體為棕褐色，頭背及頸部有縱紋，但隨著成長，縱紋會逐漸消失，只剩黑色過眼縱帶，而當年紀再增加時，連過眼黑縱帶也會跟著消失。灰腹綠錦蛇以卵生的方式生殖，於夏季產卵，每窩約可產卵5枚。
</tr>
