<td colspan="2">分佈位置:<br>
標蛇為台灣特有種蛇類，只棲息在2000公尺以上之山區，於臺灣全島中高海拔山區都有分布。為夜行性的蛇類，主要以蚯蚓為食，棲息於森林底層、落葉或腐木下等陰濕環境，隱蔽性高不易發現，諸多習性均與臺灣標蛇相似。
</td>
</tr>

<tr>
<td colspan="2">介紹:<br>
標蛇是一種小型的蛇類，最大全長可達80公分。體表為黑褐色，酷似台灣標蛇，唯體鱗僅有25列，且光滑無稜脊。 標蛇是以卵生方式生殖的蛇類，有春季產下卵7枚的紀錄。
