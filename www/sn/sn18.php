<td colspan="2">分佈位置:<br>
中國小頭蛇為游蛇科小頭蛇屬的爬行動物。分布於越南以及中國大陸的江蘇、浙江、安徽、福建、河南、廣東、廣西、貴州、雲南等地，一般生活於平原及山區。該物種的模式產地在江西廬山。2016年在馬祖有原生紀錄。
</td>
</tr>

<tr>
<td colspan="2">介紹:<br>
中國小頭蛇是一種小型的蛇類，最大全長可達72公分，牠的體型短小而圓胖，頭小型，身體背側有一條磚紅色的縱帶由頭部向後延伸至尾部。頭頸部帶有箭矢狀花紋。赤背松柏根是以卵生方式生殖的蛇類，於夏季進行生殖，每窩可產下卵3到6枚，出生仔蛇全長可達13公分。
