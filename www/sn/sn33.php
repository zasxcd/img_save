<td colspan="2">分佈位置:<br>
分佈於新竹竹東一帶山區，目前僅有6次發現記錄，可能為梭德氏帶紋赤蛇之變異個體。棲息的環境是北部中低海拔山區森林底層，夜行性為主，但也曾在白天有發現紀錄，最大全長不滿一公尺，以小型蛇類或蜥蜴為主食，卵生，數量稀少難得一見。
