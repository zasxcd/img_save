<tr>
<td colspan="2">介紹:<br>
羽鳥氏帶紋赤蛇是一種中小型的蛇類，最大全長可達95公分，其身體細長，身體有兩道紅褐色縱帶從頸部一直延伸到尾部，體側有二十餘個白點，數量稀少難得一見。性情非常溫馴，在以往的報章雜誌及網路消息中幾乎沒有聽過有人被羽鳥氏帶紋赤蛇咬傷的報導，除了數量稀少、生性隱蔽不易和人類有接觸之外，最主要的原因仍是性情溫馴，不易張口反咬禦敵，受刺激時的防禦手段大多僅是以尾巴末端角質化的尖刺來刺擊敵人。
