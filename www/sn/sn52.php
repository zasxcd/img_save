<td colspan="2">分佈位置:<br>
鎖鍊蛇為台灣特有亞種，主要分布於臺灣本島東部和南部1000公尺以下中低海拔地區，數量已極為稀少。主要棲息於山區或開墾地及河床礫灘地，常盤成一堆。鎖蛇行動緩慢，攻擊性極強，具有劇毒，能致人於死，需小心因應。夜間活動的鎖蛇，主要以蛙、蜥蜴、蛇、鳥和鼠類為食。
</td>
</tr>

<tr>
<td colspan="2">介紹:<br>
鎖鍊蛇是一種中型的蛇類，最大全長可達165公分，體型粗短，頭部呈三角形，頭和身體有許多橢圓形斑紋，故稱圓斑蝰。而且體背中央的橢圓形斑紋常前後相連，如鎖鍊般，故又稱鎖鍊蛇。鎖蛇是以胎生方式生殖的蛇類，於夏、秋之間生產，每窩可產下仔蛇20到63條。
