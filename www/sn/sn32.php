<td colspan="2">介紹:<br>
加州王蛇是游蛇科一種無毒的蛇，為王蛇的一個亞種，在自然界廣泛分布於各種棲地。加州王蛇是一種常見的寵物蛇類，以身上明顯的條帶聞名。其雖然無毒，卻能以毒蛇為食，因此對一般毒蛇的毒是免疫的。此外也以蜥蜴、鳥類與小型哺乳類為食。
