<td colspan="2">分佈位置:<br>
盲蛇主要分布於臺灣本島500公尺以下之低海拔地區，以及蘭嶼、澎湖、東沙、金門等地。也可見於東南亞地區和東非，此外，更因為人為引進而擴散至中美、佛羅里達與夏威夷。主要棲息於石頭下或土中，為典型的穴居蛇類。除了偶爾會被發現於地表活動外，多半是人們於掘土或翻動石塊時觀察到。盲蛇主要以白蟻、螞蟻或其他小型無脊椎動物為食。
</td>
</tr>

<tr>
<td colspan="2">介紹:<br>
盲蛇是一種迷你型的蛇類，最大全長不超過20公分，體色為黑色或暗褐色。眼睛極度退化的盲蛇，只剩下隱藏在鱗片下的感光眼點，尾部短而末端呈尖刺狀。盲蛇是以孤雌生殖方式生殖的卵生蛇類，性成熟體長約10公分，初生仔蛇約6公分。
