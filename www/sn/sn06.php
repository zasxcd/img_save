<tr>
<td colspan="2">介紹:<br>
史丹吉氏斜鱗蛇一種小型蛇類，最大全長可達90公分。此種蛇類體背底色個體間差異性很大，除了一般常見的褐色和灰褐色之外，還有偏紅、偏黃、偏灰、偏綠、偏橘與偏紫等等，此種後溝牙蛇類有微毒，此種蛇類為卵生，但只有很少一部分的人了解牠們的繁殖過程。當牠遇敵時，會舉起頭部與頸部，並且膨脹頸部達到威嚇效果。有點類似眼鏡蛇，可是沒有眼鏡蛇膨脹的那麼大。
