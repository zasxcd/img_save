<td colspan="2">分佈位置:<br>
台灣全島1000到2500公尺之間的中高海拔山區都有零星紀錄，晝夜皆會出沒。喜歡棲息在山林潮濕的地區，尤其是靠近河流或小溪的地方，因為牠們喜歡補食蛙類和山椒魚。
