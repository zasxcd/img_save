<td colspan="2">分佈位置:<br>
王錦蛇廣泛分布於馬祖、蘭嶼及臺灣本島2000公尺以下之中低海拔地區。此外，亦分布於中國中南部、北越以及琉球群島。主要棲息於雜木林或農墾地或住宅區，常侵入農舍，白天晚上均會活動，屬於攻擊性較強的蛇類。牠的肛門腺非常發達，在被捕捉時會發出強烈的惡臭，食性廣泛，主要以鼠類、鳥類、鳥蛋或蛙類為食，也有以蝗蟲、金龜子、蜥蜴或其他蛇類為食的紀錄。食物缺乏時，甚至有以自己幼蛇為食的記錄。
</td>
