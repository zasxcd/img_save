<td colspan="2">介紹:<br>
臭青公是一種大型的蛇類，最大體長可達265公分，頭部鱗片大且邊緣有著明顯黑色，在吻端至眼睛前緣的上方常呈王字紋，故有王錦蛇之稱。臭青公全身鱗片有明顯稜脊，故感覺粗糙而不光滑，幼蛇全身呈淺棕色，有黑色或深棕色的小斑點散雜其間，而成蛇身軀為暗褐色或橄欖綠色，夾雜了暗色與白色的雜斑，與幼蛇的花紋顯著不同。臭青公是以卵生方式生殖的蛇類，於夏季產卵，每窩可產卵8到14枚，孵化期約1個月，會有護卵的行為。
