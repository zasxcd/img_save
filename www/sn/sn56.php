<td colspan="2">分佈位置:<br>
赤尾青竹絲廣泛分布於臺灣全島和蘭嶼平地至2000公尺左右之中低海拔地區，數量相當普遍，分布很廣。此外，從中國西南、中南部地區以及吉林省東南邊、長白山、印度東北邊、緬甸、泰國至越南都有分布。主要棲息於山區或開墾地附近，喜好在稍微潮濕的環境。夜間活動的赤尾青竹絲，屬於動作雖然緩慢，但攻擊性極強的蛇類，以蛙、蜥蜴、鳥和小型哺乳類為食。雖然其毒性不強，卻仍然有致人於死的可能，需小心因應。
