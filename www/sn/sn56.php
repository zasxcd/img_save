<td colspan="2">介紹:<br>
赤尾青竹絲是一種小型的蛇類，最大全長可達90公分，其頭呈三角形，眼睛紅色，全身翠綠。因為尾巴後段磚紅色，故民間多以焦尾巴稱呼赤尾青竹絲。多數的雌蛇在身體和腹部交接處有一條白色的細縱線，而多數的雄蛇除了有此白色的細縱線外，在白線下還緊接著一條紅色的細縱線。赤尾青竹絲是以胎生方式生殖的蛇類，於秋季交配，隔年夏季生產。每窩大約可產下2到15條仔蛇，初生仔蛇全長約26公分，約1年便可達性成熟。
