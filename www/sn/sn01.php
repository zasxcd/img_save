<td colspan="2">分佈位置:<br>
金絲蛇為臺灣特有種蛇類，主要分布於臺灣北部1500公尺以下低海拔山區，在陽明山、石碇、北橫公路都有較頻繁的發現紀錄，金絲蛇棲息於潮濕的低海拔山區，是一種性情溫馴，以蝌蚪與蛙類為主食的日行性蛇類。
</td>
</tr>

<tr>
<td colspan="2">介紹:<br>
金絲蛇是一種小型的蛇類，最大體長可達60公分，體背為黑褐色，有橘黃色或橘紅色縱帶貫穿全身，而與腹鱗交接處體鱗為橘色。腹面黃色，腹鱗左右各有一個黑斑從頸部延伸至尾端。上唇鱗片為白色，頸部左右各有一個白色斑。 金絲蛇以卵生方式生殖，每窩產卵約3枚。
