<td colspan="2">分佈位置:<br>
花浪蛇廣泛分布於臺灣本島1000公尺以下中低海拔地區，以及馬祖和蘭嶼兩處離島地區。此外，中國南部地區、巴基斯坦和斯里蘭卡以及中南半島均有分布，主要棲息於山區、丘陵、農墾地，曾是水田附近的常見種類，近年來數量已大幅減少。主要為白天活動的花浪蛇，以魚類與蛙類等水邊生物為主食，也有吃蚯蚓和壁虎的紀錄。
</td>
</tr>

<tr>
<td colspan="2">介紹:<br>
花浪蛇是一種小型蛇類，最大全長可達90公分，身體前半部黑色橫帶和兩側的黃色縱帶交錯形成鐵軌狀的花紋，於身體後段，黑色橫帶則逐漸變得不明顯，而兩條黃色縱帶則更為明顯。花浪蛇為卵生的蛇類，於春末到夏季間產卵，每窩可產下4到14枚卵。
