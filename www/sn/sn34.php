<td colspan="2">分佈位置:<br>
環紋赤蛇分布於臺灣本島1000公尺以下之低海拔地區，主要棲息於山區林木底層、石縫、腐植堆裡。夜行性的環紋赤蛇動作緩慢，生性隱蔽。雖然牠的攻擊性弱，但因為為具有神經性毒的蛇類，仍應小心處理。環紋赤蛇以蜥蜴和像是盲蛇之類的小型蛇類為食。
</td>
</tr>

<tr>
<td colspan="2">介紹:<br>
環紋赤蛇是一種中小型的蛇類，最大全長可達98公分，其身體細長，全身主要由棕色與黑色相間的環紋構成，黑色環紋明顯較棕色環紋窄，且黑色環紋外緣緊連著有黃色細邊，此外頭部後方有一寬而明顯的白色環帶，是很容易辨認的蛇類。環紋赤蛇是以卵生方式生殖的蛇類，於夏季產卵，每窩可產卵4到14枚。
