<td colspan="2">分佈位置:<br>
青蛇主要分布於台灣拳道1000公使以下中低海拔地區，為日形性喜好棲息在潮濕的森林區域與農業用地附近的溫和蛇類。牠以蚯蚓以及昆蟲的幼蟲為食的習性建立了牠半樹棲的生活型態。當夜晚來臨時，喜歡在灌木或是樹梢休息。
</td>
</tr>

<tr>
<td colspan="2">介紹:<br>
青蛇為一種中型的樹棲性蛇類，最大全長可達130公分。青蛇常常被誤認為赤尾青竹絲然後遭到殺害。其實這兩種蛇很好區分，赤尾青頭部呈現三角形、有著紅色的眼睛另外兩旁體側還各有一條白線延伸到赤紅色的尾部。青蛇頭是橢圓形、黑眼睛、體側沒白線、尾巴是全綠的。另外青蛇的鱗片顏色也比較亮，赤尾青的鱗片顏色較霧。母蛇於春季到夏初之間會產下 4-13 枚 卵。初生幼體約長 26 cm。
