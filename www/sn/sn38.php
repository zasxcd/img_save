<td colspan="2">分佈位置:<br>
在臺灣地區，飯島氏海蛇只分布在綠島、蘭嶼沿岸海域；此外，向北可在琉球群島發現其分布。主要棲息環境為附近有礁石的海域，幾乎不會上岸。其毒牙已退化，攻擊性極低，對人幾乎不具有危險性。飯島氏海蛇食性特殊，專門以魚卵為食。
</td>
</tr>

<tr>
<td colspan="2">介紹:<br>
飯島氏海蛇最大全長約85公分，是一種小型的海蛇。其身體圓胖，身上的顏色主要為黑色與黃色，形成黑色和黃色相間的環帶。飯島氏海蛇具有雌雄外型二型性，雄蛇吻端會略微突出，而呈龜頭狀，故有龜頭海蛇之稱。飯島氏海蛇是以胎生方式生殖的蛇類，每次約可產下仔蛇2隻。
