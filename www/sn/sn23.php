<td colspan="2">分佈位置:<br>
茶斑蛇廣泛分布於臺灣本島以及蘭嶼1000公尺以下之中低海拔地區。此外，中國的西南和中南部地區、印度東部、尼泊爾及東南亞各國，也是茶斑蛇的分布範圍。主要棲息於山區的雜木林或農墾地附近，是有後溝牙且具有輕微毒性的蛇類。極具攻擊性的茶斑蛇，於白天、晚上都會活動，以蜥蜴和蛙類為主要食物。
</td>
</tr>

<tr>
<td colspan="2">介紹:<br>
茶斑蛇是一種中小型的蛇類，最大全長可達90公分，頭部略呈三角形，頭頂常有黑褐色Y字型斑紋。眼睛上方有明顯的稜脊突起，狀似鷹眼，因而看起來較為兇惡。全身主要為土黃色的茶斑蛇，在體背上會有一些黑色小班點散雜其間。茶斑蛇是以胎生方式生殖的蛇類，每次可以產下3到10隻仔蛇。
