<tr>
<td colspan="2">介紹:<br>
黃唇青斑海蛇身體細長，最大全長可達170公分，是一種中型蛇類。其頸部不明顯，身體的顏色主要由黑色與藍灰色的環紋所組成，且黑色環紋的寬度明顯小於藍灰色的環紋。由於上唇呈黃色，因而得名黃唇青斑海蛇。黃唇青斑海蛇是以卵生方式生殖的蛇類，會上岸在水面上的礁石縫內產卵，每窩可產卵5到6枚。
