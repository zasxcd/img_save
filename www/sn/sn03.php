<td colspan="2">分佈位置:<br>
斯文豪氏游蛇為台灣特有種蛇類，廣泛分布於臺灣本島2000公尺以下中低海拔地區，主要棲息於山區和較為潮濕的環境，為白天活動的蛇類。性情溫馴的斯文豪氏游蛇以蚯蚓為主食，雖然當受到刺激時，其頸部呈上下扁平狀，但是到目前為止，尚無此蛇危害人體的紀錄。
</td>
</tr>

<tr>
<td colspan="2">介紹:<br>
斯文豪氏是小型的蛇類，最大全長可達70公分，在頭頸部有一寬而明顯的黑色斑紋，有時略呈V字型。而眼睛斜下方和嘴角上方各有一黑色斑塊，是外型上的重要特徵。牠的身體一般為一致的棕褐色，並有黑色、土黃色的小斑點散雜其間。斯文豪氏游蛇是以卵生方式生殖的蛇類，生殖季節不明。
