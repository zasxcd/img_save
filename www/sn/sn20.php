<td colspan="2">分佈位置:<br>
赤腹松柏根主要分布於1500公尺以下地區，發現地點多在台灣中北部，南部偶有零星觀察紀錄，而東部尚未發現。此外，在中國南部地區也有發現。主要棲息於山區或開墾地底層，為一種白天活動的蛇類，嗜吃爬行動物的蛋，許多習性均與赤背松柏根類似，只是在數量上更少。在遭受攻擊時，牠的尾部會捲曲呈螺旋狀，藉以威嚇敵害。
</td>
</tr>

<tr>
<td colspan="2">介紹:<br>
赤腹松柏根是一種小型的蛇類，最大全長可達60公分，短小圓胖的體型與小型的頭部，皆與赤背松柏根相似。頭頸部有3個倒V字型斑紋，第一個斑紋穿過兩眼，身體有一些黑色環帶，腹部中央有一條赤紅色的縱帶，在紅色縱帶兩側有黑色斑塊交錯排列。生殖狀況不詳。
