<td colspan="2">分佈位置:<br>
臺灣標蛇為台灣特有亞種蛇類，只棲息在於臺灣本島中南部1000公尺以上之中高海拔山區，過去曾在南投、合歡山、嘉義奮起湖、阿里山和高雄縣出雲山林道等地有發現紀錄。於森林底層、落葉或腐木下等陰濕環境的臺灣標蛇，屬於夜行性的蛇類，主要以蚯蚓、蛞蝓為食。
</td>
</tr>

<tr>
<td colspan="2">介紹:<br>
台灣標蛇是一種小型的蛇類，最大全長可達90公分。台灣標蛇初出生時身體為黑色，而成蛇則偏橄欖綠色，前段鱗片有稜脊。牠的身體無斑紋，通體為一致性的黑色或橄欖褐色，具有27列體鱗的台灣標蛇， 於陽光照射下，體鱗會呈現金屬光澤。臺灣標蛇是以卵生方式生殖的蛇類，唯生殖季節不明。
