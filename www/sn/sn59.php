<td colspan="2">分佈位置:<br>
臺灣鈍頭蛇為台灣特有種蛇類，廣泛分布於臺灣本島平地至2000公尺之中低海拔地區。主要棲息於山區較陰濕的環境，屬於行動緩慢、性情溫和、攻擊性極低的樹棲性蛇類。臺灣鈍頭蛇在夜間活動，以蛞蝓、蝸牛為主食。
</td>
</tr>

<tr>
<td colspan="2">介紹:<br>
臺灣鈍頭蛇是一種小型的蛇類，最大全長可達70公分，全身為黃褐色，背部有不規則的黑色斜紋。牠的身體左右略為側扁，在頭頸部有W字型斑紋，眼睛較突出呈圓形，其下方有黑色斜紋，除了頭部的外型極為特殊外，臺灣鈍頭蛇是台灣的蛇類中唯一左右咽鱗不對稱的種類。臺灣鈍頭蛇是以卵生方式生殖的蛇類，於夏季產卵，每窩可產下卵2到9枚，仔蛇全長約13公分。
